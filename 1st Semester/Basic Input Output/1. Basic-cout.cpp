#include<iostream>
using namespace std;

int main() {
    int age = 18;
    int zipcode = 1930;

    cout << "This " << " is a " << "single C++ statement"; // cout is used to output text to the console
    cout << "\n";
    cout << "I am " << age << " years old and my zipcode is " << zipcode;
    cout << "\n";

    // You can also use the newline character \n to create a new line in the output
    cout << "Hi!\n";
    cout << "Hello!\nHave a great day!"; // \n is a newline character

    return 0;
}